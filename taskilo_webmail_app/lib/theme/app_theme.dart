  // Primary Colors
  static const Color primary = Color(0xFF14AD9F);
  static const Color primaryDark = Color(0xFF0D8A7F);
  static const Color primaryLight = Color(0xFF5FCFC4);

  // Background Colors
  static const Color background = Color(0xFFF5F5F5);
  static const Color surface = Colors.white;
  static const Color surfaceDark = Color(0xFF1E1E1E);
  static const Color backgroundDark = Color(0xFF121212);

  // Text Colors
  static const Color textPrimary = Color(0xFF1F2937);
  static const Color textSecondary = Color(0xFF6B7280);
  static const Color textLight = Color(0xFF9CA3AF);

  // App-spezifische Farben (wie in den Screenshots)
  static const Color emailRed = Color(0xFFEA4335);
  static const Color driveYellow = Color(0xFFFBBC04);
  static const Color photosGreen = Color(0xFF34A853);
  static const Color tasksBlue = Color(0xFF4285F4);
  static const Color calendarBlue = Color(0xFF4285F4);
  static const Color calendarRed = Color(0xFFDB4437);
  static const Color chatGreen = Color(0xFF34A853);
  static const Color meetGreen = Color(0xFF00897B);
  static const Color meetPurple = Color(0xFF7B1FA2);

  // Status Colors
  static const Color success = Color(0xFF22C55E);
  static const Color warning = Color(0xFFF59E0B);
  static const Color error = Color(0xFFEF4444);
  static const Color info = Color(0xFF3B82F6);

  // Border & Divider
  static const Color border = Color(0xFFE5E7EB);
  static const Color divider = Color(0xFFF3F4F6);
