export type CurrencyInfo = { code: string; name: string };

export function getAllCurrencies(locale: string = 'de-DE'): CurrencyInfo[] {
  let dn: Intl.DisplayNames | null = null;
  try {
    // @ts-ignore - older TS libdom may not include DisplayNames type
    dn = new Intl.DisplayNames(locale, { type: 'currency' });
  } catch {
    dn = null;
  }
  const list = CURRENCY_CODES.map(code => ({ code, name: (dn && (dn as any).of?.(code)) || code }));
  // Sort by localized name then code
  return list.sort((a, b) => (a.name || a.code).localeCompare(b.name || b.code, locale));
}
